    -e JAEGER_AGENT_HOST=jaeger \
    index.docker.io/sourcegraph/precise-code-intel-worker:3.36.1@sha256:1edc0490459ac644dca2f9bb737b1d720bd28e5eb44e55e29e2f5c97b4dba1f5

