    --name=syntect-server \
    --network=sourcegraph \
    --restart=always \
    --cpus=4 \
    --memory=6g \
    index.docker.io/sourcegraph/syntax-highlighter:3.32.1@sha256:39278362026e7f351934c174720fd2f66d87d599af3eb62b2338e23a43b8b377

echo "Deployed syntect-server service"
